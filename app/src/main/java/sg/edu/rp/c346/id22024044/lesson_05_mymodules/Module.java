package sg.edu.rp.c346.id22024044.lesson_05_mymodules;

public class Module {

    public String code;
    public String moduleName;
    public String year;
    public String sem;
    public int moduleCredit;
    public String venue;

    public Module(String code, String moduleName, String year, String sem, int moduleCredit, String venue) {
        this.code = code;
        this.moduleName = moduleName;
        this.year = year;
        this.sem = sem;
        this.moduleCredit = moduleCredit;
        this.venue = venue;
    }

}
